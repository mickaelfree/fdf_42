int	ft_isdigit(int c)
{
	return ((c >= '0' && c <= '9'));
}

int	ft_isspace(int c)
{
	return ((c == ' ' || c == '\t' || c == '\n' || c == '\r' || c == '\f'
			|| c == '\v'));
}

int	ft_ishexformat(char o,char x)
{
	return (o == '0' && (x == 'x' || x == 'X'));
}
